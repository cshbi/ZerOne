"""
클래스 기초 Step 2b — getter / setter: 값을 넣고 꺼내는 '문지기'
------------------------------------------------------------
객체에 값을 넣는 타이밍은 두 가지입니다.

  ① 태어날 때 넣는다          → 생성자 __init__        (Step 2 에서 배움)
  ② 살아가면서 넣고 바꾼다    → setter (설정 메서드)
    그리고 꺼내 읽는다        → getter (읽기 메서드)

사실 이미 만난 적이 있습니다. 지난 시간 FourCal 기억나나요?

    a = FourCal(4, 2)      # ① 생성자로 넣기
    a.setdata(9, 3)        # ② setdata 가 바로 setter!

오늘은 "언제 ①을 쓰고 언제 ②를 쓰는지" 기준을 세웁니다.
Step 1 의 Phone 그대로 시작합니다. 오늘 얹는 것: 볼륨과 문지기.
"""


class Phone:
    # ── Phone 핵심 (Step 1 그대로) ──────────────
    def __init__(self, owner, number):
        # 생성자에는 '이것 없이는 전화기라 할 수 없는' 필수 재료를 넣는다.
        self.owner = owner            # 주인 없는 폰은 없다
        self.number = number          # 번호 없는 폰도 없다
        self.power = False
        self.volume = 5               # ★ 오늘 추가: 벨소리 크기 (0~10 이라고 치자)

    def power_on(self):
        self.power = True
        print(f"[{self.owner}의 폰] 전원 ON")

    def call(self, to):
        if not self.power:
            print(f"[{self.owner}의 폰] 전원이 꺼져 있어요!")
            return
        print(f"[{self.owner}의 폰] {to} 에게 전화 📞")

    # ── 오늘의 추가: 문지기 (setter / getter) ───
    def set_volume(self, value):      # ← setter: set_ + 무엇 (들어올 때 검사)
        if 0 <= value <= 10:
            self.volume = value
            print(f"[{self.owner}의 폰] 볼륨을 {value}(으)로 설정")
        else:
            print(f"[{self.owner}의 폰] ❌ 볼륨은 0~10 사이만 됩니다 (입력: {value})")

    def get_number(self):             # ← getter: get_ + 무엇 (나갈 때 가공)
        # 전화번호를 그대로 안 주고 가운데를 가려서 준다 (개인정보 보호!)
        head, mid, tail = self.number.split("-")
        return f"{head}-****-{tail}"


# ────────────────────────────────────────────
# 1. 직접 접근의 문제: 문지기를 그냥 지나쳐 버린다
# ────────────────────────────────────────────
print("=== 직접 접근: 편하지만 위험하다 ===")
p = Phone("철수", "010-1111-1111")
p.volume = 999                        # 말도 안 되는 값인데... 아무도 못 막는다!
print("volume =", p.volume)           # 999 가 그대로 들어가 버렸다
# set_volume 이라는 문지기를 만들어 뒀어도,
# 직접 접근(p.volume = ...)은 문 옆 담장을 넘는 것과 같다.


# ────────────────────────────────────────────
# 2. setter / getter : 출입문으로 다니면 문지기가 지켜 준다
# ────────────────────────────────────────────
print("\n=== setter: 문지기가 지킨다 ===")
p = Phone("철수", "010-1111-1111")
p.set_volume(7)                       # 정상 값 → 통과
p.set_volume(999)                     # 이상한 값 → 문지기가 거부!
print("volume =", p.volume)           # 7 이 안전하게 지켜졌다

print("\n=== getter: 가공해서 꺼내 준다 ===")
print("번호:", p.get_number())        # 010-****-1111  ← 원본은 숨기고 가공본만

# 이렇게 데이터를 메서드 뒤에 두고 보호하는 것을 '캡슐화'라고 합니다.
# (지난 시간 SafefourCal 이 div 를 지켰던 것도 같은 정신입니다.
#  나중에 채팅방(Room)을 만들 때 이 단어를 다시 만납니다)


# ────────────────────────────────────────────
# 3. 그래서 언제 무엇을 쓰나? — 선택 기준
# ────────────────────────────────────────────
# ┌──────────────────────────────────────────────┬──────────────────────┐
# │ 이런 값이라면                                 │ 이걸 쓴다             │
# ├──────────────────────────────────────────────┼──────────────────────┤
# │ 태어날 때부터 없으면 말이 안 되는 값          │ 생성자 __init__       │
# │   (폰의 주인·번호, 메시지의 보낸사람)         │                      │
# │ 살면서 바뀌는 값 + 바뀔 때 검사가 필요한 값   │ setter (set_volume)  │
# │   (볼륨 0~10, 배터리 0~100)                  │                      │
# │ 읽을 때 가공·보호가 필요한 값                 │ getter (get_number)  │
# │   (번호 가리기, 여러 값 조합하기)             │                      │
# │ 검사도 가공도 필요 없는 단순한 값             │ 그냥 직접 접근도 OK  │
# │   (p.owner 읽기)                             │ (파이썬에선 자연스러움)│
# └──────────────────────────────────────────────┴──────────────────────┘
#
# 주의: 자바 같은 언어는 '무조건 getter/setter' 문화지만,
#       파이썬은 필요할 때만 만드는 게 보통입니다. 즉 '선택'입니다.
#       기준은 하나 — 그 값에 문지기(검사·가공)가 필요한가?
#
# 그리고 생성자와 setter 는 경쟁 관계가 아니라 역할 분담입니다:
#   FourCal(4, 2)    → 태어날 때 값을 갖고 태어남   (생성자)
#   a.setdata(9, 3)  → 살다가 값을 갈아끼움         (setter)


# ────────────────────────────────────────────
# 4. (한 걸음 더) @property — 파이썬다운 문지기 ※ 어려우면 넘어가도 됩니다
# ────────────────────────────────────────────
class MagicPhone:                     # 겉모습은 직접 접근, 속은 문지기 — 마법 같아서
    def __init__(self, owner):
        self.owner = owner
        self._volume = 5              # 밑줄 하나 = "직접 만지지 말아 주세요" 표시

    @property
    def volume(self):                 # getter — p.volume 이라고 '읽으면' 실행
        return self._volume

    @volume.setter
    def volume(self, value):          # setter — p.volume = 3 이라고 '쓰면' 실행
        if 0 <= value <= 10:
            self._volume = value
        else:
            print(f"[{self.owner}의 폰] ❌ 볼륨은 0~10 사이만 됩니다 (입력: {value})")


print("\n=== @property: 겉모습은 직접 접근, 속은 문지기 ===")
p = MagicPhone("영희")
p.volume = 3                          # 직접 접근처럼 보이지만 setter 가 돈다!
print("volume =", p.volume)           # 읽는 것도 getter 가 돈다
p.volume = 999                        # 문지기가 거부
print("volume =", p.volume)           # 3 이 지켜졌다
# 섹션 1 의 담장 넘기(p.volume = 999)도 @property 앞에서는 안 통합니다.

# ┌────────────────────────────────────────────────────┐
# │ 이 프로그램 한 줄 요약:                                  │
# │ 필수 재료는 생성자로, 살면서 바뀌는 값은 setter 로, │
# │ 가공해 읽을 값은 getter 로 — 문지기가 필요할 때만.  │
# └────────────────────────────────────────────────────┘

