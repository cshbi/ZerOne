"""
클래스 기초 Step 4 — 오버라이딩과 다형성: 같은 버튼, 다른 반응
------------------------------------------------------------
오버라이딩 = 부모에게 물려받은 메서드를 자식이 '자기 방식으로 다시 쓰기'.
다형성     = 종류가 달라도 '같은 이름'으로 부르면 각자 알아서 반응하는 것.

비유: 리모컨의 '재생' 버튼 하나로 TV·에어컨·선풍기를 다룬다.
     누르는 쪽은 똑같이 누르지만, 각 기기는 자기 방식대로 반응한다.
     (이 비유, week04 수업에서 다시 만납니다!)

Step 1 의 Phone 그대로 시작합니다. 오늘 얹는 것: ring, 그리고 자식들의 '다시 쓰기'.
"""


class Phone:
    # ── Phone 핵심 (Step 1 그대로) ──────────────
    def __init__(self, owner, number):
        self.owner = owner
        self.number = number
        self.power = False

    def power_on(self):
        self.power = True
        print(f"[{self.owner}의 폰] 전원 ON")

    def call(self, to):
        if not self.power:
            print(f"[{self.owner}의 폰] 전원이 꺼져 있어요!")
            return
        print(f"[{self.owner}의 폰] {to} 에게 전화 📞")

    # ── 오늘의 추가: 벨소리 ─────────────────────
    def ring(self):
        print(f"[{self.owner}의 폰] 따르릉 따르릉 ☎️")


class IPhone(Phone):
    def ring(self):                      # 부모의 ring 을 '덮어쓴다' = 오버라이딩
        print(f"[{self.owner}의 아이폰] 띠리링~ 마림바 🎵")


class GPhone(Phone):
    def ring(self):
        print(f"[{self.owner}의 갤럭시] Over the Horizon~ 🎶")


class SilentPhone(Phone):
    def ring(self):
        print(f"[{self.owner}의 폰] (무음) 지이잉- 📳")


# ────────────────────────────────────────────
# 1. 오버라이딩: 같은 이름, 다른 내용
# ────────────────────────────────────────────
print("=== 오버라이딩 ===")
Phone("아무개", "010-0000-0000").ring()      # 부모의 ring
IPhone("철수", "010-1111-1111").ring()       # 자식이 다시 쓴 ring 이 실행된다


# ────────────────────────────────────────────
# 2. 다형성: 종류를 묻지 않고 부른다
# ────────────────────────────────────────────
print("\n=== 다형성: 전화가 왔습니다! ===")
phones = [
    IPhone("철수", "010-1111-1111"),
    GPhone("영희", "010-2222-2222"),
    SilentPhone("민수", "010-3333-3333"),
    Phone("할머니", "010-9999-9999"),
]

for p in phones:
    p.ring()        # ★ 같은 한 줄인데, 네 가지 소리가 난다 ★

# 여기가 핵심입니다. for 문은 p 가 아이폰인지 갤럭시인지 전혀 묻지 않았습니다.
# 그런데도 각자 자기 벨소리를 울렸습니다.
# "어떻게 울릴지"는 각 클래스가 스스로 알고 있기 때문입니다.


# ────────────────────────────────────────────
# 3. 만약 다형성이 없었다면? (3주차의 고통을 떠올려 보세요)
