# 필요가 없기 때문에 클래스에 한 번만 새겨 둔 것입니다.


# ────────────────────────────────────────────
# 3. self 의 비밀: 사실 파이썬이 대신 넣어주는 것
# ────────────────────────────────────────────
print("\n=== self 의 비밀 ===")
p1.show_battery()          # 우리가 늘 쓰는 방식
Phone.show_battery(p1)     # 사실 위 한 줄은 이것과 완전히 같다!

# p1.show_battery() 라고 쓰면,
# 파이썬이 자동으로 p1 을 self 자리에 넣어서 Phone.show_battery(p1) 을 실행한다.
# → self 는 "점(.) 앞에 있던 그 객체" 다. 그래서 메서드 안에서
#   self.battery 라고 쓰면 '그 객체의' 배터리를 뜻하게 된다.


# ────────────────────────────────────────────
# 4. 소멸자 __del__ : 객체가 '사라질 때' 자동 실행
# ────────────────────────────────────────────
# __init__ 이 태어날 때(생성자)라면, __del__ 은 사라질 때(소멸자)입니다.

class TempPhone:
    def __init__(self, owner):
        self.owner = owner
        print(f"[{self.owner}의 폰] 개통되었습니다  (__init__ 실행)")

    def __del__(self):
        print(f"[{self.owner}의 폰] 해지되었습니다  (__del__ 실행)")


print("\n=== 생성자와 소멸자 ===")
t = TempPhone("민수")     # 태어나는 순간 → __init__
del t                     # 지우는 순간   → __del__

# del 을 직접 쓰지 않아도, 그 객체를 아무도 안 쓰게 되거나
# 프로그램이 끝나면 파이썬이 정리하면서 __del__ 을 불러 줍니다.
# 실무에서 파일 닫기 같은 '뒷정리'는 주로 with 문을 씁니다(2주차에 배운 그것!).
# 지금은 쌍으로 기억하세요: 태어날 때 __init__, 사라질 때 __del__.


