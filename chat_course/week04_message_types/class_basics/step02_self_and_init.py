"""
클래스 기초 Step 2 — self 와 __init__ 의 정체
------------------------------------------------------------
클래스에서 학생들이 가장 헤매는 두 가지: self 와 __init__.
비유: 클래스는 '붕어빵 틀', 객체(인스턴스)는 '붕어빵'.
     틀은 하나지만, 붕어빵은 팥·슈크림·피자맛 각각 다르게 구울 수 있다.

Step 1 의 Phone 그대로 시작합니다. 오늘 얹는 것: 배터리.
"""


class Phone:
    # ── Phone 핵심 (Step 1 그대로) ──────────────
    def __init__(self, owner, number):
        # __init__ = 객체가 '태어나는 순간' 자동으로 실행되는 메서드.
        #            (initialize = 초기화)  Phone("철수", ...) 하는 순간 호출된다.
        # self = 지금 태어나고 있는 '바로 그 붕어빵(객체 자신)'.
        self.owner = owner
        self.number = number
        self.power = False
        self.battery = 100            # ★ 오늘 추가: 배터리 (기본값으로 시작)

    def power_on(self):
        self.power = True
        print(f"[{self.owner}의 폰] 전원 ON")

    def call(self, to):
        if not self.power:
            print(f"[{self.owner}의 폰] 전원이 꺼져 있어요!")
            return
        print(f"[{self.owner}의 폰] {to} 에게 전화 📞")

    # ── 오늘의 추가: 배터리 확인·사용 ───────────
    def show_battery(self):
        # 메서드의 첫 번째 자리는 항상 self.
        # "누구의 배터리?" → self(자기 자신)의 배터리.
        print(f"[{self.owner}의 폰] 배터리 {self.battery}%")

    def use(self, minutes):
        self.battery -= minutes          # 자기 배터리를 깎는다
        print(f"[{self.owner}의 폰] {minutes}분 사용")


# ────────────────────────────────────────────
# 1. 붕어빵 굽기: 객체(인스턴스) 만들기
# ────────────────────────────────────────────
print("=== 인스턴스 만들기 ===")
p1 = Phone("철수", "010-1111-1111")     # 이 순간 __init__ 이 실행된다
p2 = Phone("영희", "010-2222-2222")     # 또 하나. 완전히 별개의 붕어빵.

print(p1.owner, p1.number)
print(p2.owner, p2.number)


# ────────────────────────────────────────────
# 2. 인스턴스는 서로 '독립'이다
# ────────────────────────────────────────────
print("\n=== 인스턴스는 독립 ===")
p1.use(30)             # 철수 폰만 배터리가 닳는다
p1.show_battery()      # 철수: 70%
p2.show_battery()      # 영희: 100% (그대로!)


# ────────────────────────────────────────────
# 2b. (참고) 모두가 '공유'하는 값 — 클래스 변수
# ────────────────────────────────────────────
# self.battery 처럼 붕어빵마다 다른 값은 '인스턴스 변수'.
# 반대로, 틀에 새겨져 모든 붕어빵에 똑같이 찍히는 값은 '클래스 변수'입니다.

class KoreaPhone:
    country = "대한민국"          # 클래스 변수: def 밖에, self 없이 → 모두 공유

    def __init__(self, owner):
        self.owner = owner        # 인스턴스 변수: 객체마다 각자


print("\n=== 클래스 변수: 모두가 공유 ===")
k1 = KoreaPhone("철수")
k2 = KoreaPhone("영희")
print(k1.owner, k1.country)       # 철수 대한민국
print(k2.owner, k2.country)       # 영희 대한민국 (같은 값을 본다!)

