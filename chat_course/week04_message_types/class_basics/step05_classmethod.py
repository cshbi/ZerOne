"""
클래스 기초 Step 5 — @classmethod: 객체를 만드는 '또 다른 문'
------------------------------------------------------------
week04 의 messages.py 를 읽으려면 이것 하나만 더 알면 됩니다.

지금까지 객체는 이렇게 만들었습니다:   p = Phone("철수", "010-1111-1111")
                                      → __init__ 이라는 '정문'으로 들어온 것.

그런데 재료가 항상 이렇게 깔끔하게 준비되어 있지는 않습니다.
채팅 프로그램에서는 네트워크로 이런 '한 줄 문자열'이 날아옵니다:

    "PHONE|철수|010-1111-1111"

이 문자열을 객체로 바꾸는 코드를 '어디에 둘까?' 가 오늘의 주제입니다.
Step 1 의 Phone 그대로 시작합니다. 오늘 얹는 것: show, 그리고 '또 다른 문'.
"""


class Phone:
    # ── Phone 핵심 (Step 1 그대로) ──────────────
    def __init__(self, owner, number):        # 문 1: 정문 (지금까지 쓰던 문)
        self.owner = owner
        self.number = number
        self.power = False

    def power_on(self):
        self.power = True
        print(f"[{self.owner}의 폰] 전원 ON")

    def call(self, to):
        if not self.power:
            print(f"[{self.owner}의 폰] 전원이 꺼져 있어요!")
            return
        print(f"[{self.owner}의 폰] {to} 에게 전화 📞")

    # ── 오늘의 추가: 정보 표시 + 또 다른 문 ─────
    def show(self):
        print(f"📱 {self.owner} ({self.number})")

    @classmethod
    def from_wire(cls, line):                 # 문 2: 또 다른 문 (오늘 배울 문)
        tag, owner, number = line.split("|")  # 한 줄을 쪼개 재료를 준비하고
        return cls(owner, number)             # cls(...) 로 객체를 만들어 돌려준다
        # 쪼갠 재료(owner, number)가 정문(__init__)의 재료와 똑같죠?
        # from_wire 는 결국 재료 손질 후 정문으로 안내하는 문입니다.


# ────────────────────────────────────────────
# 1. 방법 A: 클래스 '밖'의 함수로 만들기 (동작은 함)
# ────────────────────────────────────────────
def phone_from_wire(line):                    # 클래스 밖에 떨어져 있는 함수
    tag, owner, number = line.split("|")
    return Phone(owner, number)


print("=== 방법 A: 바깥 함수 ===")
p = phone_from_wire("PHONE|철수|010-1111-1111")
p.show()

# 되긴 되는데… Step 1 에서 봤던 문제가 다시 생겼습니다.
# "Phone 을 만드는 방법"인데 Phone 클래스 밖에 남남처럼 떨어져 있다.
# 클래스 = 데이터 + 동작 한 덩어리였죠? '만드는 방법'도 그 덩어리에 넣고 싶다!


# ────────────────────────────────────────────
# 2. 방법 B: @classmethod — 만드는 방법도 클래스 안에!
# ────────────────────────────────────────────
print("\n=== 방법 B: @classmethod ===")
p = Phone.from_wire("PHONE|철수|010-1111-1111")   # 객체 없이 '클래스 이름'으로 부른다!
p.show()

# 읽는 법: "Phone 아, 이 한 줄(wire)로부터 네 객체를 하나 만들어 줘."
# __init__ 이 '정문'이라면, from_wire 는 클래스가 마련해 둔 '또 다른 문'입니다.


# ────────────────────────────────────────────
# 3. 그 순간 무슨 일이? — 단계별로 따라가 보기
# ────────────────────────────────────────────
# Phone.from_wire("PHONE|철수|010-1111-1111") 한 줄의 속사정:
#
#   ① 객체가 아니라 '클래스 이름(Phone)'으로 불렀다.
#      → 아직 객체가 없으니 self 를 넣어줄 수가 없다!
#   ② 대신 파이썬이 cls 자리에 '클래스 자신(Phone)'을 넣어 준다.
#      (p1.show() 때 p1 을 self 에 넣어 주던 것과 똑같은 자동 배달)
#   ③ line.split("|") 로 재료(owner, number)를 준비한다.
#   ④ cls(owner, number) 는 곧 Phone(owner, number).
#      → 이 순간 정문(__init__)이 실행되며 새 객체가 태어난다.
#   ⑤ 완성된 새 객체를 return 으로 돌려준다.
#
# 즉 from_wire 는 __init__ 을 대신하는 게 아니라,
# "재료 손질을 먼저 한 다음, 결국 정문(__init__)으로 안내"하는 문입니다.

# ── self 와 cls 비교표 ──────────────────────
# ┌───────────────┬──────────────────┬────────────────────────────────┐
# │               │ 첫 칸에 오는 것   │ 부르는 방법                     │
# ├───────────────┼──────────────────┼────────────────────────────────┤
# │ 보통 메서드    │ self = 객체 자신 │ p.show()      ← 객체가 있어야   │
# │ 클래스 메서드  │ cls = 클래스 자신│ Phone.from_wire(...) ← 객체 없이│
# └───────────────┴──────────────────┴────────────────────────────────┘
# "객체를 만들려는 순간엔 아직 객체가 없다" → 그래서 self 가 아니라 cls 인 것!


# ────────────────────────────────────────────
# 4. 왜 Phone(...) 대신 굳이 cls(...) 라고 쓸까? — 상속과 만나면 빛난다
# ────────────────────────────────────────────
class IPhone(Phone):                          # Step 3 에서 만든 그 상속!
    def show(self):                           # Step 4 에서 배운 오버라이딩!
        print(f"🍎 {self.owner} 의 아이폰 ({self.number})")


print("\n=== cls + 상속 ===")
ip = IPhone.from_wire("PHONE|영희|010-2222-2222")
ip.show()                                     # 🍎 아이폰 버전으로 출력된다!

# from_wire 는 부모 Phone 에 '한 번만' 썼는데,
# IPhone.from_wire(...) 로 부르면 ② 단계에서 cls 자리에 IPhone 이 들어가고,
# ④ 단계의 cls(...) 가 IPhone(...) 이 되어 '아이폰 객체'가 태어납니다.
#
# 만약 cls(owner, number) 대신 Phone(owner, number) 라고 박아 썼다면?
# → 누가 불러도 항상 부모 Phone 객체만 나온다. 자식은 문을 또 만들어야 한다.
# → cls 라고 써 두면 "부른 그 클래스"의 객체가 나온다. 문 하나로 모든 자식 해결!
#   (상속 덕분에 코드는 한 번, cls 덕분에 결과는 각자 — Step 3 + 4 의 합작)


